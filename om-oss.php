<div class="OmOssArticle"><p>
Student League är en fotbollsturnering och ett fristående projekt på Halmstad Högskola som drivs av studenter under HSIFs föreningsverksamhet. Turneringen startades 2011 på bland annat Rauni Hannulas initiativ och har sedan dess växt och etablerat sig som en given aktivitet för skolans studenter.</p>

<p>Varje termin startar en ny säsong med spännande ligaspel och matcher som ger liv åt skolans studenter och folket på läktarna! Engagemanget kring Student League växer hela tiden och öppnar upp för programöverskridande gemenskaper. Många lag bildar klubbkulturer med roliga inlägg på Instagram, livesändningar och hejarklackar som lockar studenter utanför turneringen och skapar en härlig stämning.</p>

<p>Turneringen är en av få aktiviteter på Halmstad Högskola som inkluderar alla program och sträcker sig över hela terminen. Varje säsong deltar ungefär 200 studenter i 15 olika lag som utgörs av spelare från samma program eller i blandad kompott. Den största säsongen hitintills hade 31 deltagande lag och de flesta säsongerna avslutas ligaspelen med ett spännande slutspel och final.</p>

<p>Student League öppnar inte bara upp möjligheter för studenter att sparka boll och ha kul utan ger också skolans studenter möjligheten att skaffa sig meriterande erfarenheter inför arbetslivet. Att styra en förening är en berikande erfarenhet på många vis och just Student League har väldigt bra förutsättningar för dig som vill testa dina vingar, oavsett vilka ambitioner eller intressen du har.</p>

<p>V strävar efter att ständigt utvecklas och skapa en ett så framgångsrikt arrangemang det bara går. Är du intresserad av projektledning, marknadsföring, ekonomi, event, föreningslivet eller bara gemenskapen så tveka inte och hör av dig. Alla är välkomna att engagera sig i den mån de kan och känner att det är kul och vi tar gärna emot idéer och förslag kring saker vi kan göra eller förbättra!</p>
